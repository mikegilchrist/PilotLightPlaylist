def _archive_description(label):
    """Build a description string for an archive quarter playlist."""
    start, end = quarter_date_range(label)
    start_str = start.strftime("%B %Y")
    end_str = end.strftime("%B %Y")
    return (
        f"Artists who played The Pilot Light (Knoxville, TN) from "
        f"{start_str} to {end_str}. "
        f"{_VENUE_BLURB} "
        "Also check out our Upcoming Shows and Recent Shows playlists -- "
        "search Spotify for \"Pilot Light\" to find them all."
    )


def update_playlist_descriptions(sp, config, archive_quarters=None,
                                 dry_run=False):
    """Set descriptions on all managed playlists.

    Updates upcoming, recent, and any archive playlists with informational
    text about the venue and cross-references to related playlists.
    """
    prefix = "[DRY RUN] " if dry_run else ""

    # Upcoming and recent
    for key, desc in _DESCRIPTIONS.items():
        playlist_cfg = config["playlists"].get(key)
        if not playlist_cfg or not playlist_cfg.get("id"):
            continue
        log.info("%sSetting description on: %s", prefix, playlist_cfg["name"])
        if not dry_run:
            sp.playlist_change_details(playlist_cfg["id"], description=desc)

    # Archive playlists
    archives = config["playlists"].get("archives", {})
    for label, acfg in archives.items():
        if not acfg.get("id") or acfg["id"] == "DRY-RUN":
            continue
        desc = _archive_description(label)
        log.info("%sSetting description on: %s", prefix, acfg["name"])
        if not dry_run:
            sp.playlist_change_details(acfg["id"], description=desc)
